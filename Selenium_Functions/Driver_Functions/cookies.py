# A cookie in a web application is a small piece of data stored on a user's web browser.
# It's used to track and store information about the user's interactions with a website.
# Cookies help with tasks like remembering login information, user preferences, and managing user sessions.
# They come in two main types: session cookies (temporary) and persistent cookies (stored for a longer time).

# to delete the cookie added
# ==================================
# driver.delete_cookie('cookie_name')

